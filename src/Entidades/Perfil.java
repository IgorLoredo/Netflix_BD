package Entidades;

public class Perfil {
	private int user;
	private int amigoPerfil;
	private int Idioma;
	private int ResolPref;
	
	
	
	public int getResolPref() {
		return ResolPref;
	}
	public void setResolPref(int resolPref) {
		ResolPref = resolPref;
	}
	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public int getAmigoPerfil() {
		return amigoPerfil;
	}
	public void setAmigoPerfil(int amigoPerfil) {
		this.amigoPerfil = amigoPerfil;
	}
	public int getIdioma() {
		return Idioma;
	}
	public void setIdioma(int idioma) {
		Idioma = idioma;
	}
	
	
	
}
